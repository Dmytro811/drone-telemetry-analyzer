import numpy as np

def trapezoidal_integration(times, accelerations, initial_velocity=0.0):
    """
    Виконує числове інтегрування масиву прискорень для знаходження швидкості 
    за методом трапецій. Метод трапецій використовується для лінійної апроксимації площі під кривою 
    прискорення на кожному інтервалі dt. Це дає меншу похибку порівняно з 
    методом прямокутників (Ейлера).
    Математично: v(t) = v(t-1) + 0.5 * (a(t) + a(t-1)) * dt
    
    ПРИРОДА ПОХИБОК IMU:
    Оскільки швидкість отримується шляхом інтегрування прискорення, будь-який 
    постійний шум (bias) сенсора (акселерометра) при інтегруванні зростає лінійно 
    з часом, що призводить до "дрейфу швидкості" (velocity drift). Подвійне 
    інтегрування для знаходження координат призведе до квадратичного зростання 
    похибки (drift ~ t^2), тому для координат краще покладатися на GPS (ENU).
    """
    dt = np.diff(times)
    avg_accel = (accelerations[:-1] + accelerations[1:]) / 2.0
    dv = avg_accel * dt
    velocities = np.concatenate(([initial_velocity], initial_velocity + np.cumsum(dv)))
    return velocities